#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t n)
{
	size_t	i;
	size_t	j;

	j = 0;
	if (n <= ft_strlen(dest))
		return (n + ft_strlen(src));
	i = ft_strlen(dest);
	while (i < (n - 1) && src[j])
		dest[i++] = src[j++];
	dest[i] = '\0';
	return (ft_strlen(dest) + ft_strlen(src + j));
}
